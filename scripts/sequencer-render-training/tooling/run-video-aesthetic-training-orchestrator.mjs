#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { runSequencingQualityLoop } from "./run-sequencing-quality-loop.mjs";
import { buildVideoAestheticLearningBundle } from "./export-video-aesthetic-learning-bundle.mjs";

const REPO_ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "../../..");
const DEFAULT_MODEL_CATALOG = "scripts/sequencer-render-training/catalog/generic-layout-model-catalog.json";
const DEFAULT_ENDPOINT = "http://127.0.0.1:49915/xlightsdesigner/api";
const DEFAULT_OUT_ROOT = "var/logs/video-aesthetic-training-orchestrator";
const DEFAULT_BUNDLE_OUT = "apps/xlightsdesigner-ui/agent/sequence-agent/generated/video-aesthetic-learning-bundle.js";

function str(value = "") {
  return String(value || "").trim();
}

function num(value, fallback = 0) {
  const parsed = Number(value);
  return Number.isFinite(parsed) ? parsed : fallback;
}

function arr(value) {
  return Array.isArray(value) ? value : [];
}

function resolvePath(filePath = "") {
  const value = str(filePath);
  if (!value) return "";
  return path.isAbsolute(value) ? value : path.resolve(REPO_ROOT, value);
}

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, "utf8"));
}

function readJsonIfExists(filePath = "") {
  const resolved = resolvePath(filePath);
  if (!resolved || !fs.existsSync(resolved)) return null;
  return readJson(resolved);
}

function writeJson(filePath, payload) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, `${JSON.stringify(payload, null, 2)}\n`, "utf8");
}

function writeText(filePath, text) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, text, "utf8");
}

function loopDir(root = "", index = 1) {
  return path.join(resolvePath(root || DEFAULT_OUT_ROOT), `loop-${String(index).padStart(6, "0")}`);
}

function bundleModuleText(bundle = {}) {
  return `// Auto-generated by scripts/sequencer-render-training/tooling/export-video-aesthetic-learning-bundle.mjs\nexport const VIDEO_AESTHETIC_LEARNING_BUNDLE = ${JSON.stringify(bundle)};\n`;
}

function loopScore(loopRoot = "") {
  return readJsonIfExists(path.join(resolvePath(loopRoot), "video-aesthetic-score.json")) || {};
}

function loopComparison(loopRoot = "") {
  return readJsonIfExists(path.join(resolvePath(loopRoot), "video-aesthetic-attempt-comparison.json")) || {};
}

function strategyFromController(controllerState = {}) {
  const next = arr(controllerState.nextQueue)[0] || {};
  return str(next.nextStrategy || next.selectionHint);
}

function iterationSummary({ index, summary = {}, controllerState = {}, learningBundle = null, exportRef = "" } = {}) {
  const score = loopScore(summary.loopRoot);
  const comparison = loopComparison(summary.loopRoot);
  return {
    iteration: index,
    status: str(summary.status),
    loopRoot: str(summary.loopRoot),
    controllerStateRef: str(summary.controllerStateRef),
    selectedGoalId: str(summary.controllerDecision?.selectedGoalId),
    nextAction: str(summary.controllerDecision?.nextAction),
    selectionReason: str(summary.controllerDecision?.selectionReason),
    strategy: strategyFromController(controllerState),
    processedPasses: num(summary.passRunner?.processedPasses),
    acceptedEvidenceCount: num(summary.passRunner?.renderReviewAcceptedEvidenceCount),
    videoScore: {
      status: str(score.status),
      scoreBasis: str(score.scoreBasis),
      overallAestheticScore: num(score.scores?.overallAestheticScore, null),
      promotionEligible: score.promotion?.evidenceEligible === true,
      scoredWindowCount: num(score.scoredWindowCount)
    },
    attempt: {
      status: str(comparison.status),
      comparisonStatus: str(comparison.comparisonStatus),
      overallAestheticScoreDelta: num(comparison.summary?.overallAestheticScoreDelta, null),
      promotionEligible: comparison.promotionEligible === true
    },
    exportedLearning: learningBundle ? {
      exportRef,
      recordCount: num(learningBundle.recordCount),
      selectorReadyCount: num(learningBundle.selectorReadyCount)
    } : null,
    goalStatuses: arr(controllerState.goalStatuses).map((goal) => ({
      goalId: str(goal.goalId),
      evidenceStatus: str(goal.evidenceStatus),
      durableCandidateCount: num(goal.durableCandidateCount),
      blockers: arr(goal.blockers).map(str).filter(Boolean)
    }))
  };
}

function stopReasonFor({ summary = {}, index = 0, maxLoops = 1, startedAtMs = Date.now(), maxMinutes = 0 } = {}) {
  const decision = summary.controllerDecision || {};
  if (str(decision.nextAction) === "idle") return "controller_idle";
  if (str(summary.status) === "blocked_no_controller_queue") return str(decision.nextAction) === "await_evidence"
    ? "awaiting_evidence"
    : "blocked_no_controller_queue";
  if (num(maxMinutes) > 0 && Date.now() - startedAtMs >= num(maxMinutes) * 60_000) return "max_minutes_reached";
  if (index >= maxLoops) return "max_loops_reached";
  return "";
}

function bestIteration(iterations = []) {
  return [...iterations]
    .filter((row) => Number.isFinite(num(row.videoScore?.overallAestheticScore, NaN)))
    .sort((a, b) => num(b.videoScore.overallAestheticScore) - num(a.videoScore.overallAestheticScore))[0] || null;
}

function nextStepsFor(stopReason = "", best = null) {
  if (stopReason === "controller_idle") {
    return best?.exportedLearning?.selectorReadyCount
      ? ["Inspect final report and promoted video aesthetic bundle.", "Choose the next curriculum goal or broaden model/effect coverage."]
      : ["Controller is idle; inspect current curriculum coverage and existing runtime bundles.", "Choose the next curriculum goal or broaden model/effect coverage."];
  }
  if (stopReason === "max_loops_reached" || stopReason === "max_minutes_reached") {
    return ["Review best run and remaining weak dimensions.", "Resume from latestRunRoot if the trajectory is still improving."];
  }
  if (stopReason === "awaiting_evidence") return ["Provide or generate latest compact evidence before resuming."];
  return ["Review orchestrator report and controller state before resuming."];
}

export async function runVideoAestheticTrainingOrchestrator({
  seedRunRoot = "",
  previousStatePath = "",
  outRoot = DEFAULT_OUT_ROOT,
  modelCatalogPath = DEFAULT_MODEL_CATALOG,
  endpoint = DEFAULT_ENDPOINT,
  maxLoops = 20,
  maxMinutes = 0,
  maxQueue = 1,
  maxPasses = 5,
  applyRender = true,
  runType = "overnight",
  bundleOutPath = DEFAULT_BUNDLE_OUT,
  reportPath = "",
  deps = {}
} = {}) {
  const root = resolvePath(outRoot || DEFAULT_OUT_ROOT);
  fs.mkdirSync(root, { recursive: true });
  const resolvedReportPath = resolvePath(reportPath) || path.join(root, "video-aesthetic-training-report.json");
  const resolvedBundleOutPath = resolvePath(bundleOutPath || DEFAULT_BUNDLE_OUT);
  let latestRunRoot = resolvePath(seedRunRoot);
  let previousStateRef = resolvePath(previousStatePath);
  const iterations = [];
  const startedAtMs = Date.now();
  let stopReason = "max_loops_reached";

  for (let index = 1; index <= maxLoops; index += 1) {
    const runLoop = deps.runLoop || runSequencingQualityLoop;
    const summary = await runLoop({
      latestRunRoot,
      previousStatePath: previousStateRef,
      outRoot: root,
      loopRoot: loopDir(root, index),
      modelCatalogPath,
      runType,
      runId: `video-aesthetic-orchestrator-${String(index).padStart(6, "0")}`,
      maxQueue,
      maxPasses,
      applyRender,
      endpoint,
      deps: deps.loopDeps || {}
    });
    const controllerState = readJsonIfExists(summary.controllerStateRef) || {};
    let learningBundle = null;
    let exportRef = "";
    if (str(summary.status) === "executed") {
      const comparison = loopComparison(summary.loopRoot);
      if (str(comparison.comparisonStatus) === "improved" && comparison.promotionEligible === true) {
        learningBundle = (deps.buildLearningBundle || buildVideoAestheticLearningBundle)({ runRoot: summary.loopRoot });
        exportRef = resolvedBundleOutPath;
        writeText(resolvedBundleOutPath, bundleModuleText(learningBundle));
      }
      latestRunRoot = resolvePath(summary.loopRoot);
    }
    previousStateRef = resolvePath(summary.controllerStateRef);
    iterations.push(iterationSummary({ index, summary, controllerState, learningBundle, exportRef }));
    const best = bestIteration(iterations);
    const partial = {
      artifactType: "video_aesthetic_training_orchestrator_report_v1",
      artifactVersion: 1,
      generatedAt: new Date().toISOString(),
      status: "running",
      stopReason: "",
      outRoot: root,
      latestRunRoot,
      previousStateRef,
      iterationCount: iterations.length,
      bestIteration: best,
      iterations
    };
    writeJson(resolvedReportPath, partial);

    const reason = stopReasonFor({ summary, index, maxLoops, startedAtMs, maxMinutes });
    if (reason) {
      stopReason = reason;
      break;
    }
  }

  const best = bestIteration(iterations);
  const finalReport = {
    artifactType: "video_aesthetic_training_orchestrator_report_v1",
    artifactVersion: 1,
    generatedAt: new Date().toISOString(),
    status: "stopped",
    stopReason,
    outRoot: root,
    latestRunRoot,
    previousStateRef,
    bundleOutPath: resolvedBundleOutPath,
    iterationCount: iterations.length,
    bestIteration: best,
    selectorReadyExportCount: iterations.reduce((sum, row) => sum + num(row.exportedLearning?.selectorReadyCount), 0),
    iterations,
    nextSteps: nextStepsFor(stopReason, best)
  };
  writeJson(resolvedReportPath, finalReport);
  return finalReport;
}

function parseArgs(argv = []) {
  const args = {
    seedRunRoot: "",
    previousStatePath: "",
    outRoot: DEFAULT_OUT_ROOT,
    modelCatalogPath: DEFAULT_MODEL_CATALOG,
    endpoint: DEFAULT_ENDPOINT,
    maxLoops: 20,
    maxMinutes: 0,
    maxQueue: 1,
    maxPasses: 5,
    applyRender: true,
    runType: "overnight",
    bundleOutPath: DEFAULT_BUNDLE_OUT,
    reportPath: ""
  };
  for (let index = 0; index < argv.length; index += 1) {
    const arg = str(argv[index]);
    if (arg === "--seed-run-root") args.seedRunRoot = argv[++index];
    else if (arg === "--previous-state") args.previousStatePath = argv[++index];
    else if (arg === "--out-root") args.outRoot = argv[++index];
    else if (arg === "--model-catalog") args.modelCatalogPath = argv[++index];
    else if (arg === "--endpoint") args.endpoint = argv[++index];
    else if (arg === "--max-loops") args.maxLoops = Number(argv[++index]);
    else if (arg === "--max-minutes") args.maxMinutes = Number(argv[++index]);
    else if (arg === "--max-queue") args.maxQueue = Number(argv[++index]);
    else if (arg === "--max-passes") args.maxPasses = Number(argv[++index]);
    else if (arg === "--run-type") args.runType = argv[++index];
    else if (arg === "--bundle-out") args.bundleOutPath = argv[++index];
    else if (arg === "--report") args.reportPath = argv[++index];
    else if (arg === "--scaffold-only") args.applyRender = false;
    else if (arg === "--help") args.help = true;
    else throw new Error(`Unknown argument: ${arg}`);
  }
  return args;
}

function usage() {
  return `Usage:
  node scripts/sequencer-render-training/tooling/run-video-aesthetic-training-orchestrator.mjs \\
    --seed-run-root /tmp/xld-video-aesthetic-display-loop-000010 \\
    --previous-state /tmp/xld-video-aesthetic-display-loop-000010/controller-state-next-corrected.json \\
    --model-catalog /tmp/xld-vendor-fixture-model-catalog.json \\
    --max-loops 20 \\
    --max-passes 5
`;
}

const isCli = process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url);
if (isCli) {
  const args = parseArgs(process.argv.slice(2));
  if (args.help || !args.seedRunRoot) {
    process.stdout.write(usage());
    process.exit(args.help ? 0 : 1);
  }
  runVideoAestheticTrainingOrchestrator(args)
    .then((report) => process.stdout.write(`${JSON.stringify(report, null, 2)}\n`))
    .catch((error) => {
      console.error(error?.stack || String(error));
      process.exit(1);
    });
}
